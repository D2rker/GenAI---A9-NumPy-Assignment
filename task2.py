import numpy as np

A = np.array([10, 20, 30, 40])
B = np.array([1, 2, 3, 4])

# Addition
addition = A + B

# Subtraction
subtraction = A - B

# Multiplication
multiplication = A * B

# Division
division = A / B

# Power (A squared)
power = A ** 2


print(f"Addition (A + B): {addition}")
print(f"Subtraction (A - B): {subtraction}")
print(f"Multiplication (A * B): {multiplication}")
print(f"Division (A / B): {division}")
print(f"Power (A ** 2): {power}")